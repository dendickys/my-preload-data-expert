package com.example.mypreloaddataexpert.model;

public class MahasiswaModel {
    private int id;
    private String name;
    private String nim;

    public MahasiswaModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }
}
